--- @meta
--- tl.lua - Type-Lua compiler and type checker.
--- This file provides Lua Language Server definitions for the `tl` module.
--- Type-Lua (tl) is a typed dialect of Lua that compiles to regular Lua.
--- Version: 0.24.8
---
--- Configuration:
--- - `tl.path`: Can be set to override the module search path (default: uses `package.path`)
--- - `tl.package_loader_env`: Environment used by the package loader (automatically created on first use)
---
--- Usage Examples:
--- ```lua
--- -- Basic usage: check a Type-Lua file
--- local result = tl.check_string("local x: number = 42", nil, "example.tl")
--- if #result.syntax_errors == 0 and #result.type_errors == 0 then
---     print("Type checking passed!")
---     local code = tl.generate(result.ast, "5.3")
---     print("Generated Lua:", code)
--- end
---
--- -- Using the package loader
--- package.loaders[#package.loaders + 1] = tl.loader
--- local mymodule = require("mymodule") -- loads .tl files
---
--- -- Processing a file
--- local result, err = tl.process("myfile.tl")
--- if result then
---     print("File processed successfully")
--- end
--- ```
---
--- Important Notes:
--- - Type-Lua files use `.tl` extension
--- - Type definitions use `.d.tl` extension
--- - Use `local x: number = 42` for typed declarations
--- - Use `record` for defining custom types
--- - Use `global` for global type declarations

local tl = {}

--- Returns the version of the Type-Lua compiler.
--- @return string The version string (e.g., "0.24.8").
function tl.version() end

--- Parses a Type-Lua program string into an AST (Abstract Syntax Tree).
--- @param program_string string The Type-Lua source code as a string.
--- @param filename? string Optional filename for error reporting.
--- @return table AST representing the parsed program.
--- @return table errors A table of parsing errors.
function tl.parse_program(program_string, filename) end

--- A simplified parsing function for Type-Lua source code.
--- @param source_string string The Type-Lua source code.
--- @param filename? string Optional filename for error reporting.
--- @param parse_lang? string Optional language specification ("tl" or "lua").
--- @return table AST representing the parsed program.
--- @return table errors A table of parsing errors.
function tl.parse(source_string, filename, parse_lang) end

--- Generates Lua code from a Type-Lua AST.
--- @param ast table The Abstract Syntax Tree generated by `tl.parse_program`.
--- @param gen_target? string Target Lua version ("5.1", "5.2", "5.3", "5.4", "JIT").
--- @param opts? table Optional table for generation settings.
--- @return string Generated Lua code.
--- @return any|nil Generation errors if any.
function tl.generate(ast, gen_target, opts) end

--- Creates a new type reporter instance for collecting type information.
--- @return TypeReporter A new TypeReporter object.
function tl.new_type_reporter() end

--- Finds symbols in a given scope.
--- @param scope table The scope to search within.
--- @param name string The name of the symbol to find.
--- @return any The found symbol, or nil if not found.
function tl.symbols_in_scope(scope, name) end

--- Determines the Type-Lua target version based on the Lua version.
--- @param lua_version string The Lua version (e.g., "5.1", "5.3", "JIT").
--- @return string The corresponding Type-Lua target version.
function tl.target_from_lua_version(lua_version) end

--- Checks a Type-Lua string for type errors.
--- @param input string The Type-Lua source code as a string.
--- @param env? table Optional environment for type checking.
--- @param filename? string Optional filename for error reporting.
--- @param parse_lang? string Optional language specification ("tl" or "lua").
--- @return table result A table containing:
---   - `ast`: The Abstract Syntax Tree
---   - `syntax_errors`: Table of syntax errors
---   - `type_errors`: Table of type errors
---   - `warnings`: Table of warnings
---   - `env`: The environment used for checking
--- @usage
--- ```lua
--- local code = [[
---     record Point
---         x: number
---         y: number
---     end
---     
---     local p: Point = { x = 10, y = 20 }
---     print(p.x + p.y)
--- ]]
--- 
--- local result = tl.check_string(code, nil, "example.tl")
--- if #result.syntax_errors > 0 then
---     print("Syntax errors found:", #result.syntax_errors)
--- elseif #result.type_errors > 0 then
---     print("Type errors found:", #result.type_errors)
--- else
---     print("Type checking passed!")
---     local lua_code = tl.generate(result.ast, "5.3")
---     print("Generated Lua code:\n", lua_code)
--- end
--- ```
function tl.check_string(input, env, filename, parse_lang) end

--- Checks a Type-Lua file for type errors.
--- @param filename string The path to the Type-Lua file.
--- @param env? table Optional environment for type checking.
--- @param fd? userdata Optional file descriptor for the file.
--- @return table|nil result A result table (same format as `tl.check_string`), or nil on file error.
--- @return string|nil error Error message if file cannot be opened.
function tl.check_file(filename, env, fd) end

--- A custom Lua package loader for Type-Lua files.
--- This function integrates Type-Lua compilation into the Lua module loading system.
--- @return function The loaded module's main function.
--- @usage
--- ```lua
--- -- Add Type-Lua loader to package loaders
--- table.insert(package.loaders, 2, tl.loader)
--- 
--- -- Now you can require Type-Lua files
--- local mymodule = require("mymodule") -- loads mymodule.tl or mymodule.d.tl
--- 
--- -- The loader will:
--- -- 1. Search for .tl files in package.path
--- -- 2. Parse and type-check the Type-Lua code
--- -- 3. Generate Lua code
--- -- 4. Load and return the compiled module
--- ```
function tl.loader() end

--- Package loader function for Type-Lua modules.
--- This is the actual loader function used by `tl.loader()`.
--- @param module_name string The name of the module to load.
--- @return function|string loader_function The loader function, or error string with tried paths.
function tl.package_loader(module_name) end

--- Retrieves the type definitions from the Type-Lua environment.
--- @param result table The result table from `tl.check_string` or `tl.check_file`.
--- @return table types A table containing all defined types.
--- @return TypeReporter reporter The type reporter instance.
function tl.get_types(result) end

--- Initializes a new Type-Lua environment with default options.
--- @param lax? boolean Whether to enable lax mode (allow certain type errors).
--- @param gen_compat? boolean|string Whether to generate compatibility code.
--- @param gen_target? string Target Lua version for code generation.
--- @param predefined? table Predefined module definitions.
--- @return table env A new Type-Lua environment.
--- @usage
--- ```lua
--- -- Create environment for strict type checking
--- local strict_env = tl.init_env(false, "optional", "5.3")
--- 
--- -- Create environment for lax type checking (more permissive)
--- local lax_env = tl.init_env(true, "optional", "5.3")
--- 
--- -- Create environment with predefined types
--- local env_with_predefined = tl.init_env(false, "optional", "5.3", {
---     mymodule = {
---         MY_CONSTANT = "number",
---         my_function = "function(number): string"
---     }
--- })
--- ```
function tl.init_env(lax, gen_compat, gen_target, predefined) end

--- Creates a new Type-Lua environment with custom options.
--- @param opts? table Optional environment configuration.
--- @return table env A new Type-Lua environment.
function tl.new_env(opts) end

--- Performs type checking on an AST.
--- @param ast table The Abstract Syntax Tree to check.
--- @param tc_opts table Type checking options containing:
---   - `lax`: boolean - Enable lax mode
---   - `env`: table - Environment for checking
---   - `gen_compat`: boolean|string - Compatibility code generation
---   - `gen_target`: string - Target Lua version
---   - `filename`: string - Filename for error reporting
---   - `run_internal_compiler_checks`: boolean - Run internal compiler checks
--- @return table result A table containing type checking results.
function tl.type_check(ast, tc_opts) end

