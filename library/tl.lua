--- @param opts? table Type checking options.
--- @param env? table Optional environment for type checking.
--- @return table result A table containing type checking results.
--- @return string|nil error Error message if environment creation fails.
function tl.check(ast, filename, opts, env) end

--- Pretty-prints an AST for debugging purposes.
