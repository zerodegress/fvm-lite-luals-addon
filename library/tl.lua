--- Common Type-Lua Patterns:
--- 
--- 1. Basic type annotations:
--- ```lua
--- local name: string = "hello"
--- local count: number = 42
--- local active: boolean = true
--- local values: {number} = {1, 2, 3}
--- local person: {name: string, age: number} = {name = "John", age = 30}
--- ```
--- 
--- 2. Function type signatures:
--- ```lua
--- local add: function(number, number): number = function(a, b) return a + b end
--- local callback: function(string): (boolean, string?) = function(msg) return true, "ok" end
--- ```
--- 
--- 3. Record definitions:
--- ```lua
--- record Person
---     name: string
---     age: number
---     email: string?
--- end
--- 
--- local p: Person = {name = "Alice", age = 25}
--- ```
--- 
--- 4. Union types:
--- ```lua
--- local result: string | number = "hello"
--- result = 42  -- also valid
--- ```
--- 
--- 5. Generic types:
--- ```lua
--- record Box<T>
---     value: T
--- end
--- 
--- local string_box: Box<string> = {value = "hello"}
--- local number_box: Box<number> = {value = 42}
--- ```
--- 
--- Error Handling:
--- - Syntax errors: Issues with the structure of the code
--- - Type errors: Type mismatches or undefined variables
--- - Warnings: Potential issues that don't prevent compilation
--- 
--- Environment Variables:
--- - TL_PATH: Override the module search path for Type-Lua files
--- 
--- File Extensions:
--- - .tl: Type-Lua source files
--- - .d.tl: Type definition files (like .d.ts in TypeScript)
--- - .lua: Regular Lua files (can be parsed in lax mode)

