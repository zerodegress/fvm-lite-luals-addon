---@meta
--- stable.lua - Provides stable serialization and deserialization of Lua tables.

stable = {}

--- Serializes a Lua table into a string representation that can be executed as Lua code.
--- Numeric keys will be represented as `["key"] = value`, unless `nonumkey` is true.
--- Supports nested tables, numbers, booleans, strings, and nil.
---@param tbl table The table to serialize.
---@param nonumkey? boolean If true, numeric keys in the top-level table are not explicitly quoted as string keys (e.g., `value,` instead of `["1"]=value,`).
---@return string The Lua code string representing the table.
---@raise "Invalid argument. Expected a table." if `tbl` is not a table.
---@raise "Unsupported data type" if a value in the table is a type other than table, number, boolean, string, or nil.
---@raise "Unsupported key type" if a key in the table is not a number or string.
function stable.serialize(tbl, nonumkey) end

--- Deserializes a Lua code string back into a Lua table.
--- This function uses `loadstring` internally, so it should only be used with trusted input.
---@param code string The Lua code string to deserialize (e.g., typically generated by `stable.serialize`).
---@return table The deserialized Lua table.
---@raise any errors that `loadstring` and the executed code might produce.
function stable.unserlialize(code) end
